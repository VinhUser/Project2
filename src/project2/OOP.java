package project2;

/**
 *
 * @author Vinh
 */
public abstract class OOP {
    protected String ID;
    protected String name;
    
    public OOP(){
    }
    public OOP(String ID, String name) {
        this.ID = ID;
        this.name = name;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "OOP{" + "ID=" + ID + ", name=" + name + '}';
    }
    public abstract void print();
}
